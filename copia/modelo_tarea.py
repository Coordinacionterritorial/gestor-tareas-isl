# modelo_tarea.py
class Tarea:
    def __init__(self, id, estado, nombre, compromiso, terminado, delegada,
                 fecha_inicio, plazo, fecha_realizacion, observaciones):
        self.id = id
        self.estado = estado
        self.nombre = nombre
        self.compromiso = compromiso
        self.terminado = terminado
        self.delegada = delegada
        self.fecha_inicio = fecha_inicio
        self.plazo = plazo
        self.fecha_realizacion = fecha_realizacion
        self.observaciones = observaciones






